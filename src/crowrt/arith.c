int __crow_eq(int a, int b) {
  return a == b;
}

int __crow_lt(int a, int b) {
  return a < b;
}

int __crow_lte(int a, int b) {
  return a <= b;
}

int __crow_not(int x) {
  return !x;
}

int __crow_add(int a, int b) {
  return a + b;
}

int __crow_sub(int a, int b) {
  return a - b;
}

int __crow_mul(int a, int b) {
  return a * b;
}

int __crow_div(int a, int b) {
  return a / b;
}

int __crow_mod(int a, int b) {
  return a % b;
}
